public class Ex2 {
    public static void main(String[] args) {
        //while
        System.out.println("While");
        int j =10;
        while(j<10){
            System.out.print(j);
            j++;
        }
        //do while
        System.out.println("Do-While");
        int i = 10;
        do { 
            System.out.println(i);
            i++;
        } while (i < 10);
    }
}

